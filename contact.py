class Contact:

    def __init__(self, first_name, middle_name, last_name, title, company, address, mobile, email, notes):
        self.first_name = first_name
        self.middle_name = middle_name
        self.last_name = last_name
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.email = email
        self.notes = notes